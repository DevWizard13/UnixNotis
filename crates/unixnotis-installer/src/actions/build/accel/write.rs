//! Build acceleration config writes and updates

use std::fs;
use std::path::Path;

use super::model::{BuildAccelDetection, BuildAccelOutcome};
use super::wrapper::{format_build_accel_config, write_wrapper_script};

pub fn write_build_accel_config(
    repo_root: &Path,
    detection: &BuildAccelDetection,
) -> BuildAccelOutcome {
    // Keep build acceleration scoped to this repo instead of touching global Cargo state
    let config_path = repo_root.join(".cargo").join("config.toml");
    let wrapper_path = repo_root.join(".cargo").join("unixnotis-rustc-wrapper.sh");
    if config_path.exists() {
        // Rewrite only installer-owned configs so local unmanaged files stay untouched
        return update_existing_config(&config_path, &wrapper_path, detection);
    }

    // Skip setup when neither accelerator exists so no wrapper is added for nothing
    if !detection.sccache_installed && !detection.mold_installed {
        return BuildAccelOutcome::SkippedMissingTools;
    }

    let content = format_build_accel_config();
    if let Some(parent) = config_path.parent() {
        // Create `.cargo/` before any write so both wrapper and config land in one known place
        if let Err(err) = fs::create_dir_all(parent) {
            return BuildAccelOutcome::Failed(err.to_string());
        }
    }

    // Write the wrapper first so the config never points at a missing script
    if let Err(err) = write_wrapper_script(&wrapper_path) {
        return BuildAccelOutcome::Failed(err);
    }
    if let Err(err) = write_atomic(&config_path, &content) {
        return BuildAccelOutcome::Failed(err.to_string());
    }

    BuildAccelOutcome::Written {
        relative_path: ".cargo/config.toml".to_string(),
        used_sccache: detection.sccache_installed,
        used_mold: detection.mold_installed,
    }
}

fn update_existing_config(
    config_path: &Path,
    wrapper_path: &Path,
    detection: &BuildAccelDetection,
) -> BuildAccelOutcome {
    // Existing content is read first so unmanaged configs can be rejected without writes
    let existing = match fs::read_to_string(config_path) {
        Ok(existing) => existing,
        Err(err) => {
            return BuildAccelOutcome::Failed(format!(
                "Failed to read existing build config: {err}"
            ))
        }
    };
    if !existing.contains("Generated by unixnotis-installer") {
        return BuildAccelOutcome::SkippedExistingConfig;
    }

    // Rewriting installer-owned configs keeps local builds working after tool churn
    let content = format_build_accel_config();
    if let Err(err) = write_wrapper_script(wrapper_path) {
        return BuildAccelOutcome::Failed(err);
    }
    if let Err(err) = write_atomic(config_path, &content) {
        return BuildAccelOutcome::Failed(err.to_string());
    }

    BuildAccelOutcome::UpdatedExisting {
        relative_path: ".cargo/config.toml".to_string(),
        used_sccache: detection.sccache_installed,
        used_mold: detection.mold_installed,
    }
}

fn write_atomic(path: &Path, contents: &str) -> std::io::Result<()> {
    // A sibling temp file keeps rename atomic on common Unix filesystems
    let parent = path.parent().ok_or_else(|| {
        std::io::Error::new(std::io::ErrorKind::InvalidInput, "missing parent directory")
    })?;
    fs::create_dir_all(parent)?;
    let file_name = path.file_name().unwrap_or_default().to_string_lossy();
    let tmp_name = format!("{file_name}.tmp-{}", std::process::id());
    let tmp_path = path.with_file_name(tmp_name);

    // The temp file is written fully before the final rename touches the live config
    fs::write(&tmp_path, contents)?;
    fs::rename(tmp_path, path)?;
    Ok(())
}
